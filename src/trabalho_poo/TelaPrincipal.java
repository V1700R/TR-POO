    private void inputTextAreaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_inputTextAreaKeyReleased
        int ENTER_CODE = 10;
        
        String input = inputTextArea.getText();
        if (input.split("\n").length > 1 && evt.getKeyCode() == ENTER_CODE) {
            try {
                calculadora.checkInput(input.replace("=", ""));
                errorLabel.setText("");
            } catch (Exception ex) {
                errorLabel.setText(ex.getMessage());
                inputTextArea.setText(input.substring(0, input.length() - 1));
            }
        }
        
        char valorDigitado = evt.getKeyChar();
        if (valorDigitado == '=') {
            errorLabel.setText("");
            Double resultado;
            try {
                resultado = calculadora.calcular(input);
                String inputComResultado = input + "\n" + resultado;
                inputTextArea.setText("");
                resultTextArea.setText(inputComResultado);
            } catch (ZeroDivision ex) {
                errorLabel.setText(ex.getMessage());
                inputTextArea.setText(input.substring(0, input.length() - 1));
            }
        }
    }//GEN-LAST:event_inputTextAreaKeyReleased

